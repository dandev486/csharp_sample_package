using System;

namespace DanDev486.CsharpSamplePackage
{
    ///<summary>
    ///Sample math operations.
    ///</summary>
    public static class SampleMath
    {
        ///<summary>
        ///Performs summation of two integral numbers.
        ///</summary>
        ///<param name="a">First integral number.</param>
        ///<param name="b">Second integral number.</param>
        ///<returns>Summation result.</returns>
        public static int Sum(int a, int b) => a + b;

        ///<summary>
        ///Performs subtraction of two integral numbers.
        ///</summary>
        ///<param name="a">First integral number.</param>
        ///<param name="b">Second integral number.</param>
        ///<returns>Subtraction result.</returns>
        public static int Sub(int a, int b) => a - b;
    }
}
